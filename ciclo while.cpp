#include <iostream>
using namespace std;

int main() {
    int i = 1;  // Inicialización de la variable

    // Ciclo while que imprime los números del 1 al 10
    while (i <= 10) {
    //while (condición) {
        cout << "Numero: " << i << endl;
        i++;  // Incremento de la variable
    }
    
    return 0;
}
//El ciclo while es útil cuando no sabes exactamente cuántas veces se ejecutará el bloque de código, pero tienes una condición que debe cumplirse para seguir iterando.